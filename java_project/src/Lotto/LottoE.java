package Lotto;
//* 로또 등수 확인
//* 1등 : 6자리 일치
//* 2등 : 5자리 일치 + 보너스 번호 일치
//* 3등 : 5자리 일치
//* 4등 : 4자리 일치
//* 5등 : 3자리 일치
//* 나머지 꽝
public class LottoE extends Lotto {

	// 당첨번호 생성 클래스 Lotto를 상속받아서 보너스 번호만을 처리
	// 멤버변수
	private int bonus; //보너스 번호

	
	
	//메서드
	//오버라이딩 메서드 : randomLotto(), init, toString , insertNumbers
	
	@Override
	protected void randomLotto() {
		// 기존 메서드 numbers의 배열은 이미 채워진 상황
		super.randomLotto();
		while(true){
			int r = random();
			if(isContain(r)) {
				bonus = r;
				break; //값을 넣으면 무조건 브레이크로 빠져나가기 
			}
		}
		
	}

	@Override
	protected void init() {
		//numbers 배열 재생성
		super.init();
		bonus = 0; //보너스 번호 초기화
	}

	@Override
	public String toString() {
		// 보너스 번호 추가
		return super.toString()+"["+bonus+"]";
	}

	@Override
	protected void insertNumbers(int[] arr) { //7자리
		// 수동으로 7자리 배열이 들어온다면 
		super.insertNumbers(arr); //6자리 배열생성
		//마지막 한자리가 남아있는 상황
		//보너스 번호에 추가
		bonus = arr[arr.length-1];
	}

	public void insertNumbers(int[]arr, int bouns) {
		super.insertNumbers(arr);
		//보너스 번호만 추가 
		this.bonus=bonus;
	}
	
	//getter

	public int getBonus() {
		return bonus;
	}
	
		
	}
	
	
	
	
	
	
	
	
	
	
	
	


